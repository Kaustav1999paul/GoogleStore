package Model;

public class Recent {
    private String Product_Name , price , Description, Product_Image , category,pid ,Date ,Time;

    public Recent(){}

    public Recent(String product_Name, String price, String description, String product_Image, String category, String pid, String date, String time) {
        Product_Name = product_Name;
        this.price = price;
        Description = description;
        Product_Image = product_Image;
        this.category = category;
        this.pid = pid;
        Date = date;
        Time = time;
    }

    public String getProduct_Name() {
        return Product_Name;
    }

    public void setProduct_Name(String product_Name) {
        Product_Name = product_Name;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getDescription() {
        return Description;
    }

    public void setDescription(String description) {
        Description = description;
    }

    public String getProduct_Image() {
        return Product_Image;
    }

    public void setProduct_Image(String product_Image) {
        Product_Image = product_Image;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getPid() {
        return pid;
    }

    public void setPid(String pid) {
        this.pid = pid;
    }

    public String getDate() {
        return Date;
    }

    public void setDate(String date) {
        Date = date;
    }

    public String getTime() {
        return Time;
    }

    public void setTime(String time) {
        Time = time;
    }
}
